package basic.loop;

public class ContinueExample {

	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++) {
			if(i % 2 != 0) continue; // continue발동하면 밑에 코드는 실행 안하고 
			                         // 건너뛰게 된다.
			System.out.print(i + " ");
		}
		System.out.println("\n반복문 종료!");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
